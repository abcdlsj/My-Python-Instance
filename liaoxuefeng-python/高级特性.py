def findMinAndMax(L):
    if L == []:
        return(None,None)
    Max=Min=L[0]
    for n in L:
        if n>Max:
            Max=n
        if n<Min:
            Min=n
    return (Min,Max)
        
if findMinAndMax([]) != (None, None):
    print('测试失败!')
elif findMinAndMax([7]) != (7, 7):
    print('测试失败!')
elif findMinAndMax([7, 1]) != (1, 7):
    print('测试失败!')
elif findMinAndMax([7, 1, 3, 9, 5]) != (1, 9):
    print('测试失败!')
else:
    print('测试成功!')

